    private void secChangeProfLabelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_secChangeProfLabelActionPerformed
        boolean isImage = false;
        JnaFileChooser chooser = new JnaFileChooser();

        boolean a = chooser.showOpenDialog(this); //shows file explorer
        if (a) {
            String sourceFilePath = chooser.getSelectedFile().getAbsolutePath(); //gets path of image selected
            String newFileName = null; //used to temporarily keep the path of the current image to delete later if not the same extension

            try { //replaces the profile picture displayed in the menu
                BufferedImage bi = ImageIO.read(new File(sourceFilePath)); //if this errors, it will not set the isImage to true
                Image img = bi.getScaledInstance(250, 250, Image.SCALE_SMOOTH);
                ImageIcon icon = new ImageIcon(img);
                profIcon.setIcon(icon);
                isImage = true;

            } catch (NullPointerException ex) {
                JOptionPane.showMessageDialog(null, "Invalid File Type", "Message", JOptionPane.ERROR_MESSAGE);
                Logger.getLogger(MenuFrame.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(MenuFrame.class.getName()).log(Level.SEVERE, null, ex);
            }

            if (isImage) {
                // Destination directory path within your Java project
                String destinationDirectoryPath = "src/profilePictures";
                String oldFile = null;
                System.out.println(sourceFilePath);

                try {
                    String hostname = "localhost";
                    String sqlInstanceName = "NETBEANSCONNECT"; //computer name
                    String sqlDatabase = "VisitorMS";  //sql server database name
                    String sqlUser = "sa";
                    String sqlPassword = "BSIT2AG1"; //passwrod sa account

                    Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");

                    String connectURL = "jdbc:sqlserver://" + hostname + ":1433"
                            + ";instance=" + sqlInstanceName + ";databaseName=" + sqlDatabase
                            + ";encrypt=true;trustServerCertificate=true";

                    Connection conn = DriverManager.getConnection(connectURL, sqlUser, sqlPassword);
                    System.out.println("Connect to database successful!!");

                    System.out.println(SecurityID);
                    PreparedStatement pst1, pst2;
                    
                    String SqlStatement1 = "SELECT profilePicture FROM SECURITY_PERSONNEL WHERE staffid = ?";
                    pst1 = conn.prepareStatement(SqlStatement1);
                    pst1.setString(1, String.valueOf(SecurityID));
                    pst1.executeQuery();
                    ResultSet rs = pst1.executeQuery();
                    if(rs.next()){
                        oldFile = rs.getString("profilePicture"); //gets oldFilepath
                    }
                    
                    try {
                        // Create source file path object
                        Path sourcePath = Paths.get(sourceFilePath);

                        // Extract file name and extension from source file path
                        String fileName = sourcePath.getFileName().toString();
                        String extension = fileName.substring(fileName.lastIndexOf(".") + 1);

                        // New name for the copied file based on the extension of the original file
                        newFileName = "picture" + SecurityID + "." + extension;

                        // Create destination directory path object
                        Path destinationDirectory = Paths.get(destinationDirectoryPath);

                        // Create destination file path by appending the new file name to destination directory path
                        Path destinationFilePath = destinationDirectory.resolve(newFileName);

                        // Copy the file
                        Files.copy(sourcePath, destinationFilePath, StandardCopyOption.REPLACE_EXISTING);

                        // Delete previous file if different file extension
                        Path oldFilePath = Paths.get(oldFile);
                        Files.deleteIfExists(oldFilePath);

                        System.out.println("File copied successfully!");

                    } catch (IOException e) {
                        System.err.println("Error copying file: " + e.getMessage());
                    }
                    
                    String SqlStatement2 = "UPDATE SECURITY_PERSONNEL SET profilePicture = ? WHERE staffid = ?"; //updates file path in sql database
                    pst2 = conn.prepareStatement(SqlStatement2);
                    pst2.setString(1, "src/profilePictures/" + newFileName);
                    pst2.setString(2, String.valueOf(SecurityID));

                    pst2.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Profile Picture Changed Successfully", "Message", JOptionPane.INFORMATION_MESSAGE);

                    conn.close();

                } catch (SQLException | ClassNotFoundException e) {
                    System.out.println("ERror   ");
                    //e.printStackTrace();
                    //Logger.getLogger(MenuFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
                System.out.println(oldFile);       
                
            }
        }

    }//GEN-LAST:event_secChangeProfLabelActionPerformed

