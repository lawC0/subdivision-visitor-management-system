    public void updatePhoneNumber() {
        Connection conn = null;
        try {
            String hostname = "localhost";
            String sqlInstanceName = "NETBEANSCONNECT"; // computer name
            String sqlDatabase = "VisitorMS"; // SQL server database name
            String sqlUser = "sa";
            String sqlPassword = "BSIT2AG1"; // password for sa account

            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");

            String connectURL = "jdbc:sqlserver://" + hostname + ":1433"
                    + ";instance=" + sqlInstanceName + ";databaseName=" + sqlDatabase
                    + ";encrypt=true;trustServerCertificate=true";

            conn = DriverManager.getConnection(connectURL, sqlUser, sqlPassword);
            System.out.println("Connect to database successful!!");

            String newPhoneNumber = secPhoneField2.getText();

            // Check if the new phone number already exists
            int phoneCheck = checkPhone(newPhoneNumber);

            if (newPhoneNumber.length() != 11 || !newPhoneNumber.matches("\\d+")) {
                JOptionPane.showMessageDialog(null, "Invalid phone number. It must be 11 digits long and contain only numbers.", "Error", JOptionPane.ERROR_MESSAGE);
            } else if (phoneCheck == 1) {
                // Phone number exists
                JOptionPane.showMessageDialog(null, "Phone Number already in use.", "Error", JOptionPane.ERROR_MESSAGE);
            } else if (phoneCheck == 0) {
                // Phone number does not exist, proceed to update
                String updateQuery = "UPDATE SECURITY_PERSONNEL SET PhoneNumber = ? WHERE StaffID = ?";
                PreparedStatement pst = conn.prepareStatement(updateQuery);
                pst.setString(1, newPhoneNumber);
                pst.setString(2, String.valueOf(SecurityID));
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Phone Number Changed Successfully", "Message", JOptionPane.INFORMATION_MESSAGE);
                secPhoneField1.setText(secPhoneField2.getText());
                secPhoneField2.setText("");
            } else {
                // Error during phone number check
                JOptionPane.showMessageDialog(null, "An error occurred while checking the phone number.", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (SQLException | ClassNotFoundException e) {
            JOptionPane.showMessageDialog(null, "An error occurred while updating the phone number.", "Error", JOptionPane.ERROR_MESSAGE);
        } finally {
            if (conn != null) {
                try {
                    conn.close();
                } catch (SQLException e) {
                    e.printStackTrace();
                }
            }
        }
    }

